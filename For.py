
#Doesn't count the last number.
for element in range(10): 
    print(element)

#Starts from 'a' until 'b', doesn't count the last number.
for element in range(1, 10): 
    print(element)

#You can use Lists/ Arrays, Tuples, Dictionaries, etc.
list = ["a", "b", "c", "d", "e", "f", "g"]
for letter in list: 
    print(letter)