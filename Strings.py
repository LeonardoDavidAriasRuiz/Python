#Declaration
firstName = "Leonardo"
lastName = 'Arias'

#link 2...n strings
fullName = firstName + " " + lastName
print(fullName)

mon = "monday"
tue = "tuesday"
week = "Week: {}, {}.".format(mon,tue)
print(week)

week = f"Week days: {mon} and {tue}."
print(week)

#How to use "" or '' in a string
quote = "I'm dancing"
print(quote)

quote = 'She said "Hello"'
print(quote)
